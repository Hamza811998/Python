# Variables: Objects containing specific values
x = 5 # Numeric or integer variable
print(x)

y="We are learning Python with Hamza" # String Variable
print(y)

x=x+10 # or 5+10=15
print(x)

# Types/Class of Variables
type(x)
print(type(x)) # Class 'int'
print(type(y)) # Class 'str'

# Rules to assign a variable
#   1. The variable should contain letters, numbers or underscores_ 
#   2. Do not start with numbers
#   3. Spaces are not allowed
#   4. Do not use keywords used in functions (break, mean, median, test, lambda, for, continuous etc...)
#   5. Short & descriptive
#   6. Case sensitivity (Lowercase, uppercase letters; I suggest always use lowercase letters)

# Example:-

# fruit_basket="Mangoes", "Oranges"
